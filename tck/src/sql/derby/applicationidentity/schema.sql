);

CREATE TABLE VersionedPCPoint (
    ID BIGINT,
    X INTEGER NOT NULL,
    Y INTEGER,
    VERSION INTEGER,
    CONSTRAINT VERSIONED_PCPNT_CONST PRIMARY KEY (ID)
